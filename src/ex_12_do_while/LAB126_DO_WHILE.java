package ex_12_do_while;

public class LAB126_DO_WHILE {
    public static void main(String[] args) {


        int i = 0;
        //do while loop
        //if you want to execute any particular bloac of code for atleast one time, you can use do while loop


        do {
            System.out.println("hello");
            System.out.println(i);
            i++;
        } while (i < 0);
        //while loop
        while(i<0){
            System.out.println(i);
        }
    }
}


